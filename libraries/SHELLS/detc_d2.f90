!=======================================================================
  Real(8) Function detc_d2 (id,it)
!=======================================================================
! coefficient for determinamt id and term it for subshell d2 
!-----------------------------------------------------------------------
 
  Implicit none
 
  Integer, intent(in)  :: id,it
 
  Integer, parameter :: kd_d2 =  45
  Integer, parameter :: nt_d2 =   5
 
  Integer :: INT_d2 (kd_d2,nt_d2)
 
  Integer :: Norm_d2  = 70
 
